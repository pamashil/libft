#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		len;
	char	*dest;

	dest = (char *)malloc(sizeof(*src) * ft_strlen((char *)src) + 1);
	if (dest == NULL)
		return (NULL);
	len = 0;
	while (src[len] != '\0')
	{
		dest[len] = src[len];
		len++;
	}
	dest[len] = '\0';
	return (dest);
}
