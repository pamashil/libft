#include "libft.h"

char	*ft_strchr(char const *s, int c)
{
	char	*st;

	st = (char *)s;
	while (*st != c)
	{
		if (*st == '\0')
		{
			return (NULL);
		}
		st++;
	}
	return (st);
}
